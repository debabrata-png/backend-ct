const mongoose=require('mongoose');

const prtemplateschema = new mongoose.Schema({
    name: {
        type: String,
        required: [true,'Please enter name']
    },
    user: {
        type: String,
        required: [true,'Please enter user'],
        unique: false
    },
    colid: {
        type: Number,
        required: [true,'Please enter colid']
    },
    template: {
type: String
},
category: {
type: String
},
description: {
type: String
},
type: {
type: String
},
level: {
type: String
},
status1: {
        type: String
    },
    comments: {
        type: String
    }
})
//
const prtemplate=mongoose.model('prtemplate',prtemplateschema);

module.exports=prtemplate;

